import 'dart:io';

void main() {
  String mensagem;
  mensagem = "Hello, world!";
  print(mensagem);
  double contador = 100;

// Atribuição de soma
  contador += 10;
  print(contador);

// Atribuição de subtração
  contador -= 5;
